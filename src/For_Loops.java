
public class For_Loops {
	public static void main(String[] args) {
		for (int y = 0; y <= 100; y++) {
			System.out.print(y + " ");
		}
		System.out.println();
		for (int u = 100; u >= 0; u--) {
			System.out.print(u + " ");
		}
		System.out.println();
		for (int h = 2; h <=100; h+=2) {
			System.out.print(h + " ");
		}
		System.out.println();
		for (int v=1; v<=99; v+=2){
			System.out.print(v+" ");
		}
		System.out.println();
		
		for (int n=1; n<=500; n++){
			if (n%2==1){
				System.out.println(n+" Odd");
			}
			else{
				System.out.println(n+" Even");
			}
			
			
		}
		
		
		
		
		
		
		
		
		
		
	}
}
